
package net.mcreator.elemantalimitation.enchantment;

import net.minecraftforge.registries.ObjectHolder;

import net.minecraft.inventory.EquipmentSlotType;
import net.minecraft.enchantment.EnchantmentType;
import net.minecraft.enchantment.Enchantment;

import net.mcreator.elemantalimitation.ElemantalImitationModElements;

@ElemantalImitationModElements.ModElement.Tag
public class FurnacingEnchantment extends ElemantalImitationModElements.ModElement {
	@ObjectHolder("elemantal_imitation:furnacing")
	public static final Enchantment enchantment = null;
	public FurnacingEnchantment(ElemantalImitationModElements instance) {
		super(instance, 37);
	}

	@Override
	public void initElements() {
		elements.enchantments.add(() -> new CustomEnchantment(EquipmentSlotType.MAINHAND).setRegistryName("furnacing"));
	}
	public static class CustomEnchantment extends Enchantment {
		public CustomEnchantment(EquipmentSlotType... slots) {
			super(Enchantment.Rarity.RARE, EnchantmentType.DIGGER, slots);
		}

		@Override
		public int getMinLevel() {
			return 1;
		}

		@Override
		public int getMaxLevel() {
			return 1;
		}

		@Override
		public boolean isTreasureEnchantment() {
			return true;
		}

		@Override
		public boolean isCurse() {
			return false;
		}

		@Override
		public boolean isAllowedOnBooks() {
			return true;
		}
	}
}
